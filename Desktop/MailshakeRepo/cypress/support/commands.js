Cypress.Commands.add('clickOnElement', (index) => {
    cy.get('#mat-input-'+[index])
})

Cypress.Commands.add('checkURL', (url) => {
    cy.url().should('include',(url))
})

Cypress.Commands.add('getNavItem', (index) => {
    cy.get('[class="mat-list-item-content"]').eq([index]).click()
})